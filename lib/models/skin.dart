// models/skin.dart

class Skin {
  final String name;
  final String type;
  final String releaseDate;
  final String availability;
  final String price;
  final String imagePath;

  Skin({
    required this.name,
    required this.type,
    required this.releaseDate,
    required this.availability,
    required this.price,
    required this.imagePath,
  });
}
